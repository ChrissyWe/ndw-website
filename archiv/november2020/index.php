                  Digital Citizen Science, eine neue Forschungsmethode, die wir in Karlsruhe entwickeln und etablieren wollen, könnte die Lösung sein. Dabei begreifen wir Bürger*innen wie 
                  dich als Citizen Scientistis, die nicht nur an Studien teilnehmen sondern selbst aktiv in verschiedenen Forschungsprojekten mitwirken. So kann die unmittelbare Nähe zu 
                  vielen gesellschaftlichen Fragen und die Kreativität und Kompetenz der ganzen Gesellschaft die Forschung bereichern. 
