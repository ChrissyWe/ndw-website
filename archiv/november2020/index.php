              <p>12,8 % des Transportaufkommens in Deutschland wird über die Binnenschifffahrt abgewickelt. Im ersten Moment scheint diese Zahl im Vergleich zu Straße und Schiene 
              gering zu sein, doch im Gesamtkontext betrachtet wird schnell klar, welche Auswirkungen diese auf den Wirtschaftsstandort Deutschland hat. Ein Binnengüterschiff hat eine 
              vergleichbare Transportkapazität, wie z. B. 40 LKW oder ein Güterzug. Der Energieverbrauch in Dieselkraftstoff eines Schiffes für 100 Tonnenkilometer ist nachweislich geringer 
              als bei anderen Verkehrsträgern. Nachhaltigkeit und Klimawandel sind in unserer Gesellschaft wichtige Themen, daher sollte es unser Ziel sein, diesen Verkehrsweg aufrecht zu 
              erhalten. Schleusenanlagen machen deutsche Wasserstraßen erst schiffbar, weswegen deren Zustand entscheidend dafür ist, ob Schifffahrt überhaupt möglich ist. 
              Das durchschnittliche Alter der vom Bund betriebenen Einkammerschleusen liegt bei ca. 97 Jahren. Um Betriebsausfälle zu vermeiden, wurde in den letzten Jahren auf 
              aufwendige und komplexe Ersatzneubauten zurückgegriffen. Im Rahmen des hier vorgestellten Forschungsprojekts werden Verfahren entwickelt, die eine Instandsetzung der alten 
                                    Bausubstanz unter laufendem Betrieb ermöglichen.</p>
