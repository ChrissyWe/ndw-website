              <p>Die molekulare Schere CRISPR/Cas: Teufelszug oder Revolution für die Pflanzenzüchtung?
                In diesem Jahr wurde die Entdeckung der molekularen DNA-Schere CRISPR/Cas9 mit dem Nobelpreis für Chemie gewürdigt. Keine Entdeckung der letzten 30 Jahre hat die Biologie, 
                die Medizin und die Landwirtschaft bereits so verändert, wie wir das gerade dank CRISPR/Cas erleben. Aber was für Konsequenzen, 
                was für Risiken bringt diese Technik, mit der man die genetische Information fast beliebig verändern kann, mit sich? 
                Mit CRISPR/Cas konnten bereits neue hochwertige Kulturpflanzensorten hergestellt werden, die eine verbesserte Fruchtqualität aufweisen oder gegen verschiedene 
                Krankheitserreger wie Mehltau resistent sind. So ist eine Reduktion des Pestizidverbrauchs in der Landwirtschaft möglich. Zurzeit sind nicht Gluten-freie,
                sondern auch um die negativen Folgen der globalen Erwärmung abzumildern, Hitze- und Salzstress-resistente Pflanzen, in der Entwicklung. 
                Aber ist das nicht Alles zu schön um wahr zu sein? Erzeugen wir nicht Monster, die wir nicht mehr kontrollieren können? </p>
              <p>Referent*in: Prof. Dr. Holger Puchta, <a href="https://www.botanik.kit.edu/molbio/index.php" target="_blank">Lehrstuhl für Molekularbiologie und Biochemie</a></p>
