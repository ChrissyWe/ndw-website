                                    komplexer Netzwerke sehen, welches nützliche Werkzeug dieses
                                    vermeintliche Luftschloss tatsächlich ist. Fragen, die man damit
                                    beantworten kann, reichen von "Warum kennt man jede Person über
                                    nur ein paar Ecken?" über "Wie verbreiten sich Viren oder
                                    Gerüchte?" bis hin zu "Warum kann mein Computer das so schnell
