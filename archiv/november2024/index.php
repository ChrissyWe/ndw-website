                                    Annahmen, die nichts mit der Realität zu tun haben, und fragen:
                                    "Was wäre, wenn?" Und dann zermartern sie sich das Hirn, um
                                    allerlei Dinge zu beweisen, die in der resultierenden
                                    hypothetischen Welt gelten. In diesem Vortrag tauchen wir in die
                                    hypothetische Welt der hyperbolischen Geometrie ein, in der etwas
                                    sehr Ungewöhnliches für die Parallelität von Geraden
                                    gilt. Zunächst bauen wir ein Luftschloss und genießen die
                                    Schönheit (und Abstrusität) der resultierenden Theorie, losgelöst
