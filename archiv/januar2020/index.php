                                Menschzentrierte Digitalisierung mit interaktiven, intelligenten Systemen </a></h4>
                    </div>
                    <div id="collapseOne3" class="panel-collapse collapse">
                        <div class="panel-body">
                            <div class="img-container"><img src="./images/vortrag/31.png"/><br/>
