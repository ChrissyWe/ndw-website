            <h2>Was ist die Nacht der Wissenschaft? </h2>
            <p> In der Nacht vom 24. auf den 25. Januar 2020 findet zum fünften Mal die „Nacht der Wissenschaft” am KIT
                statt. In spannenden Vorträgen geben Dozenten aus verschiedenen Fachbereichen der Karlsruher Hochschulen
                dabei Einblicke in ihr Themengebiet. Zwischen den Vorträgen können sich Besucher und Dozenten bei Essen
                und warmen/kalten Getränken über die neuen Erkenntnisse austauschen. </p>
            <p> Daher laden wir euch ein, uns bis in die frühen Morgenstunden auf eine Reise durch die Wissenschaft zu
                begleiten… </p>
