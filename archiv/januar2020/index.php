                            <p>Brillengläser gehören zu den Gegenständen, auf die wir uns stets verlassen. Seit dem
                                Mittelalter diente Mineralglas als Material und das optische Design blieb bis ins 20.
                                Jahrhundert im Wesentlichen unverändert (siehe Foto). In den letzten 100 Jahren nahm das
                                Brillenglas eine rasante technische Entwicklung. Streng genommen sind Rezeptgläser die
                                Vorreiter der Industrie 4.0, denn seit 20 Jahren zeigen sie deren Konzept:
                                Hochindividualisierte Massenproduktion. Die technischen Anforderungen sind enorm, denn
                                Individualisierung, geringe Masse, Kratzfestigkeit & Entspiegelung schließen sich in der
                                selben Materialgruppe für den Alltagseinsatz aus. Nur die geschickte Kombination von
                                organischen, anorganischen & hybriden Materialien ermöglichen die geforderte
                                Funktionalität.</p>
