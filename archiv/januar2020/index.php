                                Wie bringe ich Licht in die Chemie? - Photokatalyse in der organischen Synthese </a>
                        </h4>
                    </div>
                    <div id="collapseOne5" class="panel-collapse collapse">
                        <div class="panel-body">
                            <div class="img-container"><img src="./images/vortrag/22.jpg"/><br>
                            </div>
                            <p>Die meisten konventionellen chemischen Reaktionen in der organischen Synthese werden mit
                                Wärme angetrieben. Die Verwendung von Licht als Enerqiequelle bietet die Chance, die
                                Energiewende auch in der Chemie voranzutreiben. Wie bringe ich das Licht in die
                                Synthese? Eigentlich wird dafür das energiereiche UV-Licht benötigt. Viel nachhaltiger
                                wäre die Verwendung des sichtbaren Lichtes, weil es den Hauptanteil des Sonnenlichts
                                darstellt und LED-Aufbauten als Energiequelle für das chemische Labor preiswert zur
                                Verfügung stehen. Allerdings ist die Energie des sichtbaren Lichtes viel zu gering, um
                                die meisten organisch-chemischen Bindungen zu brechen (und zu knüpfen), und damit
                                eigentlich für die Synthese von Naturstoffen, Wirkstoffen oder Materialien ungeeignet.
                                Lösungen bietet die chemische Photokatalyse mit Metallverbindungen oder organischen
                                Farbstoffen. Wie das gelingt, wird im Vortrag besprochen.</p>
                            <p>Referent: Prof. Dr. Hans-Achim Wagenknecht, <a href="https://www.ioc.kit.edu/"
