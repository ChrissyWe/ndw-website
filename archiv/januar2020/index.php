                            <p>Sowohl der Knochen als auch der Terminator sind auf allen Ebenen so konzipiert, dass sie
                                ihre Funktionen erfüllen können und sind sehr widerstandsfähig. Daher befindet es sich
                                im Inneren einer Legierung (eine organische Matrix mit eingebetteten Mineralpartikeln),
                                die Steuerung wird von Mikroprozessoren (Osteozyten) übernommen, aber die Umgebung
                                besteht aus lebendem Gewebe: Fleisch, Haut, Haare, Blut. Darüber hinaus heben beide die
                                Fähigkeit zur Selbstreparatur nach Beschädigungen. Wie genau das geschieht und was der
                                Knochen noch so für tolle Eigenschaften hat, wird in diesem Vortrag gezeigt, bei dem die
                                Grenzen zwischen Biologie, Medizin, Physik und Materialwissenschaften verschwinden.</p>
                            <p>Referentin: Victoria Schemenz (Alumni), <a href="https://www.mpikg.mpg.de/"
                                                                          target="_blank">Max-Planck-Institut für
                                    Kolloid- und Grenzflächenforschung</a></p>
