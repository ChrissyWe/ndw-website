                                Periodische und fast-periodische Strukturen </a></h4>
                    </div>
                    <div id="collapseOne12" class="panel-collapse collapse">
                        <div class="panel-body">
                            <div class="img-container"><img src="./images/vortrag/34.svg"/>
                                Bild: Creative Commons 3.0 BY-SA, Claudio Rocchini
                            </div>
                            <p>Symmetrie ist ein zentrales Paradigma in der modernen Mathematik, aber auch in den
                                Naturwissenschaften, den bildenden Künsten und der Musik. Bis weit in die zweite Hälfte
                                des 20. Jahrhunderts hinein ging man daher davon aus, dass sich auch Materie stets in
                                symmetrischen Strukturen organisiert. Shechtmans Entdeckung von Quasikristallen
                                (Chemie-Nobelpreis 2011) führte zu einem radikalen Paradigmenwechsel: Neben periodischen
                                Strukturen, die ein maximales Maß an Symmetrie aufweisen, tauchen in der Natur auch
                                fast-periodische Strukturen auf, die keinerlei Symmetrien besitzen. Mathematiker haben
                                diese Strukturen schon Jahrzehnte vorher beschrieben. Wir wollen uns der Geschichte
                                hinter der Entdeckung dieser Strukturen bildlich und spielerisch nähern. </p>
