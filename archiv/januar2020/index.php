                            <p>Intelligente Systeme spielen heute bei der Digitalisierung von Wirtschaft und
                                Gesellschaft eine zentrale Rolle. Intelligente Systeme sammeln Daten aus ihrer Umgebung,
                                verarbeiten und erzeugen Wissen, und adaptieren kontinuierlich ihr Verhalten. Dabei
                                kommen insbesondere Technologien der Künstlichen Intelligenz, wie beispielsweise
                                Maschinelles Lernen oder Sprachverarbeitung, zur Anwendung. Das Zusammenspiel zwischen
                                Mensch und intelligenten Systemen geht jedoch mit zahlreichen Herausforderungen einher.
                                Um das positive Potenzial intelligenter Systeme für Wirtschaft und Gesellschaft zu
                                entfalten, ist daher ein menschzentriertes Paradigma bei der Gestaltung interaktiver
                                intelligenter Systeme zentral. Im Vortrag werden ausgewählte Grundkonzepte und aktuelle
                                Forschungsaktivitäten am KIT im Bereich der menschzentrierten Digitalisierung mit
                                interaktiven intelligenter Systeme vorgestellt.</p>
