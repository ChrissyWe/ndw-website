                                <div class="hoersaal color2"> 20:00 HS B.</div>
                                Mit BioMEMS zu neuartigen Produkten in den Life-Sciences und der Biomedizintechnik </a>
                        </h4>
                    </div>
                    <div id="collapseOne2" class="panel-collapse collapse">
                        <div class="panel-body">
                            <div class="img-container"><img src="./images/vortrag/21.png"/><br/>
                                Bild: Mikrofluidisches System zur Analyse von Zellen
                            </div>
                            <p>Der Begriff BioMEMS (Biomedical-Micro-Electro-Mechanical-Systems) umfasst neuartige
                                Gerätschaften aus den Bereichen der Life-Sciences und der Biomedizintechnik.
                                Innerhalb der Arbeitsgruppe „BioMEMS – Biomedizinische Mikrotechnik“ werden am KIT obige
                                Systeme intensiv erforscht.<br/>
                                Dabei liegt der Fokus der Forschungs- und Entwicklungsarbeit auf den
                                fertigungstechnischen Aspekten, aber auch die neuartigen Anwendungsmöglichkeiten für
                                sog. mikrofluidische Systeme, wie Lab-on-Chip-Systeme oder Organ-on-Chip-Systeme, werden
                                untersucht.
                                Ein wichtiges Thema hierbei ist in Zukunft die Bereitstellung geeigneter Chipsysteme für
                                miniaturisierten Bioreaktoren zur Untersuchung von Zellen (vgl. Abbildung).<br/>
                                Auch biomedizintechnische Implantate, welche in mannigfaltiger Art und Weise in den
                                menschlichen Körper implantiert werden können, lassen sich mit einer Kombination aus
                                mikrotechnischen Fertigungsmethoden und mikroelektronischer Technologien fertigen.
                                Dabei kann eine Aufteilung zwischen einfach aufgebauten Implantaten und s.g. smarten
                                Implantaten vorgenommen werden.
                                Typische Beispiele stellen u.a. Stent-basierte Systeme, (implantierbare)
                                Drug-Delivery-Systeme, Herzschrittmacher sowie diverse Neuroprothesen, wie z.B.
                                Cochlea-Implantate oder Retina-Implantate dar.</p>
                            <p>Referent: Prof. Dr. Andreas E. Guber, <a href="https://www.imt.kit.edu/61.php"
                                                                        target="_blank">IMT - AG BioMEMS</a></p>
