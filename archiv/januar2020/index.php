                            <p>Die Weltbevölkerung wächst seit Jahrzehnten stetig an. Gleichzeitig steigt der
                                wirtschaftliche Wohlstand. Beide Entwicklungen führen zu einem zunehmenden Druck auf
                                unsere natürliche Umwelt, unser Klima und unsere Ressourcen. Wir greifen tief in
                                bestehende Ökosysteme ein, der Klimawandel zeugt davon. Ressourcen wie Sand, Kupfer,
                                Zink oder Helium werden bald nicht mehr technisch, ökologisch und ökonomisch sinnvoll
