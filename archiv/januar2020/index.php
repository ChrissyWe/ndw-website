                                temporäre Lagerstätte von Rohstoffen in einem Kreislaufsystem. Gleichzeitig müssen wir
                                vermehrt eine Verlagerung hin zum regenerativen Anbau, zur Zucht und Kultivierung von
                                Ressourcen und Baumaterialien anstreben, anstatt uns weiterhin auf endliche Vorkommen zu
                                verlassen. </p>
