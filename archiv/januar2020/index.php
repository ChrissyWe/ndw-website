                                Die Zukunft der Holzfeuerung - mehr Schutz von Mensch und Umwelt/Klima durch Sensor
                                geführte Heizsysteme
                            </a></h4>
                    </div>
                    <div id="collapseOne4" class="panel-collapse collapse">
                        <div class="panel-body">
                            <div class="img-container"><img src="./images/vortrag/12.jpg"/></div>
                            <p>Wärmegewinnung aus Holz steht neben anderen regenerativen Energieträgern zur nachhaltigen
                                Energieversorgung im Fokus. In diesem Vortrag werden zunächst die besonderen
                                Abbrandbedingungen von Scheitholz und die daraus resultierende Problematik hoher,
                                toxischer Emissionen von Klein-Feuerungsanlagen privater Haushalte erläutert. Dabei
                                werden auch einige Tipps zur Vermeidung unnötiger Umweltbelastung durch umweltbewusstes
                                Betreiberverhalten vorgestellt.
                                Wie erreicht man gute Scheitholzverbrennung?
                                Ein aus wissenschaftlichen Erkenntnissen abgeleitetes Sensor gestütztes automatisiertes
                                Verbrennungsluft- Regelungsverfahren wird vorgestellt, das aktuell vor der Felderprobung
                                steht.<br>
                                Anhand von am Institut für Sensor- und Informationssysteme durchgeführten
                                experimentellen Untersuchungen wird aufgezeigt, dass dieses neu entwickelte
                                Verbrennungsluft-Regelungsverfahren in Zukunft zu bedeutenden Emissionsminderungen von
                                Scheitholz-Einzelraumfeuerstätten und Heizkesseln führen kann.<br>
                                Anhand von vergleichenden Abgasanalysedaten (vor/nach der Nachrüstung) wird
                                demonstriert, dass es auf diese Weise gelingen kann, die Emissionen insbesondere im
                                Anfahrverhalten des Verbrennungsprozesses erheblich zu reduzieren.
                                Der Vortrag schließt mit einigen Gedanken zur dringenden Notwendigkeit der Verminderung
                                der Emissionen durch Wärmegewinnung aus Biomasse im Kontext mit den Bemühungen zum
                                Klimaschutz.</p>
                            <p>Referent: Prof. Dr. Heinz Kohler, <a href="https://www.hs-karlsruhe.de/isis/"
                                                                    target="_blank">ISIS Hochschule Karlsruhe</a></p>
