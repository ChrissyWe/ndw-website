                                <div class="hoersaal color2"> 22:00 HS B.</div>
                                „Und was hat das mit Wissenschaft zu tun?“ Aushandlungen über gute Wissenschaft in
                                Blogpostdiskussionen </a></h4>
                    </div>
                    <div id="collapseOne8" class="panel-collapse collapse">
                        <div class="panel-body">
                            <div class="img-container"><img src="./images/vortrag/23.jpg"/></div>
                            <p>Wissenschaftliche Erkenntnisse, Theorien und Ansätze einer breiten Öffentlichkeit zu
                                vermitteln, ist im Internetzeitalter längst nicht mehr ausschließlich Aufgabe von
                                Wissenschaftsjournalist*innen. Immer mehr Wissenschaftler*innen,
                                Wissenschaftskommunikator*innen und Wissenschaftsinteressierte nutzen digitale Formate
                                wie beispielsweise Weblogs, um ihre Begeisterung für Wissenschaft mit anderen zu teilen.
                                Das Besondere an diesen Formaten ist, dass sie die Möglichkeit zum Austausch mit anderen
