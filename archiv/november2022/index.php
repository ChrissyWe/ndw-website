                                Produktion von „grünem“ Wasserstoff aus regenerativ erzeugtem Strom steht die Elektrolyse
                                im Fokus. Die verschiedenen Typen von Elektrolyseuren, die heute industrialisiert werden,
                                unterscheiden sich im Hinblick auf Leistungsfähigkeit, Effizienz und Kosten. Im Hinblick
                                auf die Nutzung von Wasserstoff rücken – neben dem seit langem etablierten, direkten
