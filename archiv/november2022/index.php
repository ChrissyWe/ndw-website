                                realisiert werden kann. Hier sind Systeme für stationäre Energieversorgung und Elektromobilität
                                in Entwicklung. <br> Der Vortrag gibt einen Überblick über verschiedene Elektrolyseur- und
                                Brennstoffzellentechnologien und diskutiert Vor- und Nachteile im Hinblick auf Produktion
                                und Nutzung regenerativ erzeugten Wasserstoffs. </p>
