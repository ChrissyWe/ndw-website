                            <p>Was haben Bier, Sauerteig, laktosefreie Milch, eine saubere Hose und ein COVID-19 Medikament gemeinsam?<br>
                                Ihr Dasein verdanken sie einer Stoffumwandlung, die mit Hilfe biologischer Katalysatoren erfolgt.
                                Für die Verbesserung dieser Biokatalysatoren haben wir uns eine der erfolgreichsten Methoden bei
                                der Natur abgeschaut: Das Auswählen des Systems mit den geeignetsten Eigenschaften aus der natürlich
                                und zufällig entstehenden Diversität und Vielfalt – das Prinzip der Evolution.<br>
